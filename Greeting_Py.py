# A simple Python program

# Variables
name = "Alice"
age = 25

# Function
def greet(person):
    print(f"Hello, {person}!")

# Conditional
if age >= 18:
    print(f"{name} is an adult.")
else:
    print(f"{name} is a minor.")

# Loop
for i in range(5):
    print(f"Counting: {i}")

# Calling the function
greet(name)
